"""
contracts.py
============
Generic, YAML-driven data contract validation engine.
Adapts to read YAML contracts generated by the agents, running validation checks
over Spark DataFrames using performant aggregated checks.
"""
from __future__ import annotations

import os
import glob
from typing import Dict, Any, List, Tuple

import yaml
from pyspark.sql import DataFrame
from pyspark.sql import functions as F

